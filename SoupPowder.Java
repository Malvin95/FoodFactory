
public class SoupPowder extends NonPerishable{
  
  private String Name;
  private int time;
  private Boolean isSpoilt;
  private double prob;
  
  public SoupPowder()
  {
    Name = "Cheese";
    time = 45;
    isSpoilt = false; 
  }
  
  public String getName()
  {
    return Name;  
  }
  
  public int getTime()
  {
    return time;
  }
  
  public boolean checkIfSpoilt()
  {
	  return isSpoilt;
  }
  
  public double checkProb()
  {
    return prob;
  }
  
}
